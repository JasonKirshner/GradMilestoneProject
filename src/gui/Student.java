    public Student(Person user) {
        this.user = user;
        initComponents();
        nameLabel.setText("Welcome " + user.getFirstName() + " " + user.getLastName());
        
        project = new Project();
        if (user.getProjectForStudent() > -1) {
            project.setProjectID(user.getProjectForStudent());
            project.fetch();

            projectNameText.setText(project.getName());
            projectDescriptionText.setText(project.getDescription());
            projectStartText.setText(project.getStartTerm() + "");
            projectEndText.setText(project.getDeadline());
            projectStatusText.setText(project.getCurrentStatus());
            
            updateGroupMembers();
        } else {
            JOptionPane.showMessageDialog(null, "Sorry, there are no projects associated with your account.");
            System.exit(0);
        }
        
    }

