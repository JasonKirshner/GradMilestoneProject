    private void updateGroupMembers() {
        people = project.getStudentsFromProject();
        projectGroupMembers.setText("");
        for (Person person : people) {
            projectGroupMembers.append(person.getFirstName() + " " + person.getLastName() + "\n");
        }
    }
    
